CREATE TABLE IF NOT EXISTS `ps_coins_products` (
	`id` INT(11) NOT NULL AUTO_INCREMENT,
	`name` VARCHAR(255) NOT NULL,
	`index` VARCHAR(255) NOT NULL,
	`box` VARCHAR(255) NOT NULL,
	`category` VARCHAR(255) NOT NULL,
	`type` VARCHAR(255) NOT NULL,
	`image` VARCHAR(255) NULL DEFAULT NULL,
	`weapon` VARCHAR(255) NULL DEFAULT NULL,
	`price` INT(11) NOT NULL DEFAULT '0',
	`pricereal` INT(11) NOT NULL DEFAULT '0',
	`created` DATETIME NOT NULL DEFAULT current_timestamp(),
	PRIMARY KEY (`id`) USING BTREE
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS `ps_coins_my_products` (
	`id` INT(11) NOT NULL AUTO_INCREMENT,
	`user_id` INT(11) NULL DEFAULT NULL,
	`item_id` VARCHAR(255) NOT NULL,
	`created` DATETIME NOT NULL DEFAULT current_timestamp(),
	PRIMARY KEY (`id`) USING BTREE
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS `ps_coins_my_skins` (
